// 段階A（issue #13）: 我々の Annex C 実装が生成した DeviceRequest を、Multipaz の
// DeviceRequestParser（JVM・org.multipaz:multipaz-jvm）に食わせてクロス検証するための
// フィクスチャを書き出す。エミュレータ不要・決定的。
//
// 出力: interop/multipaz-jvm/fixture.json（Kotlin テストが読む）
//   deviceRequestHex     … 我々の verifier が組む DeviceRequest(CBOR) — readerAuth 署名つき
//   sessionTranscriptHex … Annex C の ["dcapi", hash] 形（Multipaz parse に渡す）
//   expected             … 我々の主張（docType/要素/readerAuthenticated=true）
//   readerRootPem        … Multipaz の TrustManager に入れる Reader CA（我々の trust list アンカー）
import { writeFileSync, mkdirSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { X509Certificate } from 'node:crypto';
import { VerifierService } from '../src/verifier.mjs';
import { parseDeviceRequest } from '../src/device-request.mjs';
import { annexCSessionTranscript } from '../src/handover.mjs';
import { fromB64url, hex } from '../src/cbor.mjs';

const root = (r) => fileURLToPath(new URL('../' + r, import.meta.url));
const out = root('interop/multipaz-jvm');
mkdirSync(out, { recursive: true });

const v = new VerifierService({}); // Node: pki/reader を遅延読込して readerAuth 署名
const { request, origin } = await v.createRequest({
  specs: [{ id: 'q1', configId: 'pid_mdoc', claims: ['family_name', 'age_over_18'] }],
  protocol: 'annex-c',
});

const drBytes = fromB64url(request.deviceRequest);
const st = annexCSessionTranscript({ base64EncryptionInfo: request.encryptionInfo, serializedOrigin: origin });
const parsed = parseDeviceRequest(drBytes).docRequests[0];

const fixture = {
  note: 'Generated by scripts/export-multipaz-fixture.mjs — our Annex C DeviceRequest, cross-checked by Multipaz',
  origin,
  deviceRequestHex: hex(drBytes),
  sessionTranscriptHex: hex(st),
  encryptionInfoB64: request.encryptionInfo,
  expected: {
    docType: parsed.docType,
    namespaces: Object.keys(parsed.nameSpaces),
    entryNames: parsed.nameSpaces,          // { ns: { element: intentToRetain } }
    readerAuthPresent: !!parsed.readerAuth,
    readerAuthenticated: true,              // Multipaz が我々の readerAuth 署名を検証して true になるべき
  },
  readerRootPem: new X509Certificate(readFileSync(root('pki/reader/reader-ca.crt'))).toString(),
};

writeFileSync(`${out}/fixture.json`, JSON.stringify(fixture, null, 2) + '\n');

// 負例: 要求項目を差し替え、readerAuth は元のまま温存した DeviceRequest。
// Multipaz は ReaderAuthentication 再構成が合わず readerAuthenticated=false にする（or throw）べき。
const { buildItemsRequestBytes } = await import('../src/device-request.mjs');
const { cborEncode } = await import('../src/cbor.mjs');
const tamperedItems = buildItemsRequestBytes({ docType: parsed.docType, elements: { [parsed.docType]: { portrait: true } } });
const tamperedDr = cborEncode(new Map([
  ['version', '1.0'],
  ['docRequests', [new Map([['itemsRequest', tamperedItems], ['readerAuth', parsed.readerAuth]])]],
]));
writeFileSync(`${out}/fixture-tampered.json`, JSON.stringify({
  note: 'Items swapped, original readerAuth kept — Multipaz must NOT authenticate the reader.',
  deviceRequestHex: hex(tamperedDr),
  sessionTranscriptHex: hex(st),
}, null, 2) + '\n');

// readerAuth 省略版: reader 鍵を渡さず組んだ DeviceRequest（18013-5 上 optional）。
// Multipaz は parse は成功しつつ readerAuth=null / readerAuthenticated=false にするはず。
const { buildDeviceRequest } = await import('../src/device-request.mjs');
const noReaderDr = buildDeviceRequest({
  docType: parsed.docType,
  elements: parsed.nameSpaces,
  sessionTranscriptBytes: st,
  // readerKeyPem/readerCertDer を渡さない → readerAuth を付けない
});
writeFileSync(`${out}/fixture-noreader.json`, JSON.stringify({
  note: 'DeviceRequest without readerAuth (reader authentication omitted). Multipaz should parse it but not authenticate the reader.',
  deviceRequestHex: hex(noReaderDr),
  sessionTranscriptHex: hex(st),
}, null, 2) + '\n');

console.log('wrote', `${out}/fixture.json (+tampered, +noreader)`);
console.log('  deviceRequest', drBytes.length, 'bytes / readerAuth', fixture.expected.readerAuthPresent);
